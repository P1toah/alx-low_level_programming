#include "3-calc.h"
/**
 *op_mod - modolo of a and b
 *@a: first number
 *@b: second number
 *
 *Return: result
 */
int op_mod(int a, int b)
{
return (a % b);
}
/**
 *op_div - division of a and b
 *@a: num 1
 *@b: num 2
 *
 *Return: result
 */
int op_div(int a, int b)
{
return (a / b);
}
/**
 *op_mul -mutiples a and b
 *@a: num 1
 *@b: num 2
 *
 *Return: result
 */
int op_mul(int a, int b)
{
return (a * b);
}
/**
 *op_sub - substacts a from b
 *@a: num 1
 *@b: num 2
 *
 *Return: result
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 *op_add - adds a and b
 *@a: num 1
 *@b: num 2
 *
 *Return: result
 */
int op_add(int a, int b)
{
return (a + b);
}
